package Chapter1;

/**
 * Program will print the perimeter and area of two double variable
 *
 * @author Trey Lavender
 */
public class C1_8 {

    /**
     * Main Method
     *
     * @param args arguments for command box
     */
    public static void main(String[] args) {
        double a = 5.5;
        double b = 22 / 7;
        System.out.println("The perimeter is " + 2 * a * b + ".");
        System.out.println("The area is " + a * a * b + ".");
    }

}
